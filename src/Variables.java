public class Variables {
    public static void main(String[] args) {
        // int, short, long, chr, String, boolean
        short abc = 123; // 2^15
        int x = 5; // 2^31
        long y = 4000000000L; // 2^63 maximum size of long
        System.out.println(x);
        System.out.println(y);
        System.out.println(abc);

    }
}